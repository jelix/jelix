        $db->exec('DROP TABLE IF EXISTS article3serial');
        $db->exec('CREATE TABLE "article3serial" ("id" integer, "category_id" integer NOT NULL, "title" varchar(200) NOT NULL, "content" text NOT NULL)');

        $context = new DaoContext($db->getSQLType());
